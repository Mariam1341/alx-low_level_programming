unsigned int _strspn(char *s, char *accept)
{
	unsigned int i = 0, check = 0, k, j;
	
	for (k = 0; accept[k] != '\0'; k++); // get accept lengh
	
	while (s[i] != '\0')
	{
		for (j = 0; accept[j] != '\0'; j++)
		{
			if (s[i] == accept[j])
			{
				check++;
				break;
			}
			else if (accept[j + 1] == '\0')
				return (check);
		}
		i++;
	}
	return (check);
	
}
